// goto Statement

#include<iostream> 
using namespace std;
int main() 
{
     cout<<"This program explains the use of goto statements \n";

     goto abc;

     cout<<"Programming in C++ \n";
     cout<<"This is our Programming Fundamental Course \n";

     abc:
     cout<<"Program is terminated \n";
     cout<<"Good Bye \n";

     return 0;
}